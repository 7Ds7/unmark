		<p>You can also add links to Nilai using a browser extension. As of today I've only put together <a href="http://dl.dropbox.com/u/5443042/nilai_extension.crx">a beta version of an extension for Google Chrome</a>.</p>

		<p>Once you have the bookmarklet or extension installed it is very easy to add a link to Nilai. When you're on a page that you'd like to save for later or share with a group - click the bookmarklet or the extension button and that link is added to Nilai. It is that simple!</p>

		<p><em>A tip!</em> Want to practice? Add this page to Nilai!</p>

		<h3>Labels and Groups</h3>
		<p>Adding a label to a link or adding a link to a group is as easy as one click or tap. But first, why add labels?</p>

		<p>Labels are used to separate your links based on what you'd like to do with them later. Perhaps you're reading a long article that you'd like to finish later. Adding it to the Read label means that you'll remember later what it was that you'd like to accomplish with the link. Similarly, adding the labels Watch or Listen may indicate that you'd like to Watch a video or Listen to a bit of audio later.</p>

		<p>Groups are used for sharing links with a group of people. Perhaps you work at a company and you spend a lot of time during the day going through many emails from coworkers sending you links all day. Or, maybe you'd like to share a recipe with your wife or a link to vacation photos with extended family. Groups make it as easy as 1 tap or click to share with as many people as you'd like.</p>

		<p>When you add a link to Nilai you can add a label or add the link to a group simply by clicking on the button to do so. However, if you've already added a link to Nilai you can click on the <i class="icon-info-sign"></i> Info button to edit the links details.</p>

		<h3>Using Nilai on the go</h3>
		<p>If you have an Internet-enabled smartphone you can use Nilai. Simply point your phone's browser to nilai.co and sign in the same as you would on your computer.</p>

		<h4>Adding links to Nilai from your phone</h4>
		<p>Unfortunately this isn't nearly as easy as it should be. Adding bookmarklets to Apple's iPhone or iPad or Android-powered devices is just difficult. We're currently working on building a Nilai application for the iPhone to make this much easier.</p>
		<p>If you'd like to add links from your phone please see <a  href="/help/bookmarklet">the bookmarklet instructions help article</a>.</p>

		<h4>Adding Nilai to your iPhone/iPad home screen</h4>
		<p>I recommend doing this. Log into nilai.co and, once logged in, click the Add Bookmark button. One of the selections will be "Add to Home Screen". This will make it very easy to get back to Niali.</p>

